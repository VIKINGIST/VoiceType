_current_lang = "uk"


def set_language(lang: str) -> None:
    """Встановити мову UI."""
    global _current_lang
    _current_lang = lang


def t(key: str, **kwargs) -> str:
    """Отримати переклад за ключем. Fallback: uk → en → key."""
    entry = STRINGS.get(key, {})
    text = entry.get(_current_lang) or entry.get("uk") or entry.get("en") or key
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text
